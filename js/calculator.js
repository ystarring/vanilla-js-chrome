const calculator = {
  plus: function(a, b) {
    console.log(a + b);
  },
  minus: function(a, b) {
    console.log(a - b);
  },
  multi: function(a, b) {
    console.log(a * b);
  },
  divide: function(a, b) {
    console.log(a / b);
  },
};

